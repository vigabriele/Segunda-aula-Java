let data = "março";

switch (data) {
    case "março":
        console.log("começa o outono");
        break;
    case "junho":
        console.log("começa o inverno");
        break;
    case "setembro":
        console.log("começa a primavera");
        break;
    case "dezembro":
        console.log("começa o verão");
        break;

    default:

        console.log("Essa data não esta na lista");
}